n1 = input('Digite algo que desvendarei o que é: ')
print('O tipo primitivo desse valor é: {}'.format(type(n1)))
print('Só tem espaços? {}'.format(n1.isspace()))
print('É numérico? {}'.format(n1.isnumeric()))
print('É alfabético? {}'.format(n1.isalpha()))
print('É alfanumérico? {}'.format(n1.isalnum()))
print('Está em letras maiúsculas? {}'.format(n1.isupper()))
print('Está em letras minúsculas? {}'.format(n1.islower()))
print('Está captalizada? {}'.format(n1.istitle()))


